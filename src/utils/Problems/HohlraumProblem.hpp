# ifndef __HOHLRAUM_PROBLEM_HPP__
# define __HOHLRAUM_PROBLEM_HPP__

# if SPACE_DIMS == 2 || defined (DOXYCOMPILE)


# include <memory>

# include "utils/Mollifier.hpp"
# include "utils/Problems/Problem.hpp"


//------------------------------------------------------------------------------------------------------------
//! \brief  Hohlraum problem.
//!
//! \todo   Description of hohlraum test problem.
//------------------------------------------------------------------------------------------------------------
class HohlraumProblem :
    public Problem
{

public:

    //========================================================================================================
    //=== CONSTRUCTORS AND DESTRUCTOR ========================================================================
    //========================================================================================================

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Delete default constructor.
    //--------------------------------------------------------------------------------------------------------
    HohlraumProblem( void ) = delete;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Delete copy constructor.
    //--------------------------------------------------------------------------------------------------------
    HohlraumProblem( const HohlraumProblem & ) = delete;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Construct object from given parameters.
    //--------------------------------------------------------------------------------------------------------
    HohlraumProblem( const ParameterList & );

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Destructor for HohlraumProblem class.
    //--------------------------------------------------------------------------------------------------------
    ~HohlraumProblem( void ) override;


    //========================================================================================================
    //=== OPERATOR OVERLOADS =================================================================================
    //========================================================================================================

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Delete copy assignment operator.
    //--------------------------------------------------------------------------------------------------------
    HohlraumProblem & operator=( const HohlraumProblem & ) = delete;


    //========================================================================================================
    //=== PUBLIC INTERFACE ROUTINES ==========================================================================
    //========================================================================================================

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the string descriptor of the class type.
    //--------------------------------------------------------------------------------------------------------
    static const std::string Descriptor( void );

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the string descriptor for an object's type.
    //--------------------------------------------------------------------------------------------------------
    const std::string GetDescriptor( void ) const override;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Prints a summary of the problem configuration to the logging interface.
    //--------------------------------------------------------------------------------------------------------
    void Print( const std::string & = "  " ) const override;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Overrides values in given ParameterList with requirements imposed by test problem.
    //--------------------------------------------------------------------------------------------------------
    void OverrideOptions( ParameterList & ) const override;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Sets inflow boundary conditions for the test problem.
    //--------------------------------------------------------------------------------------------------------
    virtual void SetInflowBoundaries( RKDG::DensityFunction & ) const override;


protected:

    //========================================================================================================
    //=== PROTECTED INTERFACE ROUTINES =======================================================================
    //========================================================================================================

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the value of the total cross section without mollification at the specified position.
    //--------------------------------------------------------------------------------------------------------
    virtual double TotalCross(
        const double x,
        const double y
    ) const override;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the value of the scattering cross section without mollification at the specified
    //!         position.
    //--------------------------------------------------------------------------------------------------------
    virtual double ScatterCross(
        const double x,
        const double y
    ) const override;

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the feature size of the source term.
    //--------------------------------------------------------------------------------------------------------
    virtual double SourceFtSize( void ) const override {  return 1.0;  }

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the feature size of the total cross section.
    //--------------------------------------------------------------------------------------------------------
    virtual double TotalCrossFtSize( void ) const override {  return 1.0;  }

    //--------------------------------------------------------------------------------------------------------
    //! \brief  Returns the feature size of the scattering cross section.
    //--------------------------------------------------------------------------------------------------------
    virtual double ScatterCrossFtSize( void ) const override {  return 1.0;  }

};


# endif // if SPACE_DIMS == 2

# endif // ifndef __HOHLRAUM_PROBLEM_HPP__
